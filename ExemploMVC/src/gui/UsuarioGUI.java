    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Usuario us = new Usuario();
        us.setNome(txtNome.getText());
        us.setCpf(fTxtCpf.getText());
        us.setEmail(txtEmail.getText());
        us.setTelefone(fTxtTelefone.getText());
        
        if ((txtNome.getText().isEmpty()) || (fTxtCpf.getText().isEmpty()) || (txtEmail.getText().isEmpty()) || (fTxtTelefone.getText().isEmpty()) ){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        }else{
            UsuarioDao ud = new UsuarioDao();
            ud.adiciona(us);
            
            JOptionPane.showMessageDialog(null, "Usuário " + txtNome.getText() + " inserido com sucesso!");
        }
        
        txtNome.setText("");
        txtEmail.setText("");
        fTxtCpf.setText("");
        fTxtTelefone.setText("");
        
    }//GEN-LAST:event_btnSalvarActionPerformed

    
